package org.example;

public class Calculator {

    public static int sum(int numberA, int numberB) {
        int sum = numberA + numberB;
        return sum;
    }


    public static int subtract(int numberA, int numberB) {
        int subtract = numberA - numberB;
        return subtract;
    }

    public static int multiplication(int numberA, int numberB) {
        int multiply = numberA * numberB;
        return multiply;
    }

    public static double divide(int numberA, int numberB) {
        double quotient = numberA / numberB;
        return quotient;
    }
}

